      <p>
      We started our trip by booking a Trolley Tour with <a href="https://www.trolleytours.com/st-augustine" target="_blank">Old Town Trolley Tours</a> so we could get a feeling of the city and decide what other places we wanted to check out throughout our vacation. There are two competing trolley companies so definitely check out prices/stops- we preferred Old Town, but you might like <a href="https://www.ripleys.com/redtrains/" target="_blank">Ripley’s Red Trains</a>. Let me give you a word of warning- the discounts these trolley places advertise on their website are not as good as the discounts you will receive in a flyer before your trip. You will actually save $1-2 more if you want to book, unless you want to book a huge museum package.
      </p>
      <p>
      Anyway- we had a blast on the trolley tour! It was super packed on a Saturday but there was literally no parking available downtown so we would not have been able to see the sights on the weekend without the trolley. We got aboard at the <a href="https://www.visitstaugustine.com/thing-to-do/old-jail" target="_blank">Old Jail</a>, while we didn’t tour it- there was plenty to see outside including beautiful plants, an old cage (it was Summit’s favorite), the trolley depot, a gift shop and nice bathrooms. 
      </p>
      <p>
      We traveled all throughout the city and marked the stops we wanted to come back to on another day! One thing that kind of bummed me out was that we missed visiting their historical fort: <a href="https://www.nps.gov/casa/planyourvisit/conditions.htm" target="_blank">Castillo de San Marcos</a> because of the government shutdown! I would definitely recommend checking it out, if it’s open while you’re there.
      </p>
