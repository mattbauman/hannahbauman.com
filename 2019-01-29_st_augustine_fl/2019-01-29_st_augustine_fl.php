      <p>
      George Street is a walking, outdoor shopping mall. It’s the weirdest combination of very classy stores and cheap souvenir stories- I’ve never seen such a wide variety of shopping all in one place. We throughly enjoyed checking out <a href="https://www.kernelpoppers.net" target="_blank">Kernel Popper’s</a> - a popcorn store with over 250 flavors of popcorn, <a href="http://www.spanishbakerycafe.com" target="_blank">The Spanish Bakery</a> - for super delicious and homemade baked goods and <a href="http://www.myersjewelryandgifts.com" target="_blank">Myer’s Jewelry</a> - a small but super unique jewelry store with unique jewelry mementos to take home from your trip! 
      </p>
      <p>
      There is a lot of really good food in St. Augustine!! I think we only tried the tip of the iceberg but here were our favorites:
      <ul>
        <li>
        <a href="https://thedonutexperiment.com/locations/" target="_blank">The Donut Experience</a> - for custom, made-right-in-front-of-you donuts. Good thing there isn’t one of these around us!! 
        </li>
        <li>
        <a href="https://mellowmushroom.com/location/fl-saint-augustine-410-anastasia-blvd-32080/" target="_blank">Mellow Mushroom</a> - this is a small chain, but there pizza is amazing :) 
        </li>
        <li>
        <a href="https://www.obisfillinstation.com/menu.html" target="_blank">Obi’s</a> - a restaurant in an old gas station. We had breakfast- the food was great and the service was fast! 
        </li>
        <li>
        <a href="https://www.mangomangos.com" target="_blank">Mango Mango</a> - I had the most delicious mango, salmon salad there. We actually went twice and I got the salad twice- that is saying something!!
        </li>
        <li>
        <a href="https://maplestreetbiscuits.com" target="_blank">Maple Street Biscuit Company</a> - a fun breakfast place near George Street with a definite college vibe. 
        </li>
        <li>
        <a href="https://hookedonharrys.com/menu-st-augustine/" target="_blank">Harry’s Restaurant</a> - a cajun restaurant near George Street. Very delicious but spicey food. If you aren’t great with spices like me, ask for them on the side. They have a great outdoor eating space. 
        </li>
      </ul>
      </p>
      <p>
      All in all we really enjoyed St. Augustine… it might not be my favorite place to visit in Florida, but it is definitely worth the trip. If you are a history buff- this is the place for you!! 
      </p>
