      <p>
      We also enjoyed the <a href="https://www.alligatorfarm.com" target="_blank">Alligator Farm</a>- it was fun for our whole family. We had the opportunity to see the alligator bellow. They make this super deep, loud noise while “talking” to each other. I’ve never been so curious and creeped out at the same time in life! But it was awesome to see all the alligators, crocodiles, birds and other wildlife in one place. 
      </p>
