      <p>Another highlight our trip was visiting Glensheen Mansion for their annual <a href="https://duluthwintervillage.com" target="_blank">Duluth Winter Village</a> celebration. For one weekend in December, the Glensheen grounds are open to the public for this artist-driven event. Local shops set up mini "houses" to show off their wares, along with a food tent with local treats and free s'more-making! Honestly- I thought this was more fun for us than Bentleyville. It was way less crowded and Summit loved being up close to the waves. I hope the city and Glensheen keep this event going each year!</p>
      <p>We also had the opportunity to eat at <a href="https://omcsmokehouse.com" target="_blank">OMC Smokehouse</a> for the first time- OMG- it was good! OMC Smokehouse specializes in smoked meats, along with tasty BBQ sauces. We enjoyed our meal and were surprised by the extremely fast and kind service- even though the restaurant was packed! While we waited for our table, we ventured around the block and were pleasantly surprised to meet a bear inside of the Knife River Store- how fun :) </p>
